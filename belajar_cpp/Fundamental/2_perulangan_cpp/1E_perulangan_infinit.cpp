#include <iostream>

int main()
{   
    // perulangan infinit while
    while (true)
    { // true or 1
        std::cout << "infinite loop";
    }

    // perulangan infinit for
    for (;;)
    {
        std::cout << "infinite loop";
    }

    // perulangan infinit for
    for (int i = 1; i > 0; i++)
    {
        std::cout << "infinite loop";
    }

    return 0;
}